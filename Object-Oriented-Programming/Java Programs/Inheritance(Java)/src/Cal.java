public class Cal {

   int Sum(int x, int y)
   {
       return x+y;
   }
   
   int Sub(int x, int y)
   {
       return x-y;
   }
   
   int Mul(int x, int y)
   {
       return x*y;
   }
   
   int Dvd(int x, int y)
   {
       return x/y;
   }
   
}